package org.yui.base.enums;

/**
 * 二进制枚举。
 *
 * 定义一个枚举，其值包含A,B,C,D，
 *
 * 如果数据库里面要同事保存A+B的情况，则 可以让这个枚举实现这个接口。
 *
 * 定义A的二进制值是1，B是2，C是4，D是8。
 * A+B的情况，保存到数据库里面就是数字 3
 * A+C-----------------------------5
 * C+D-----------------------------12
 * A+B+D---------------------------11
 * A+B+C+D-------------------------15
 *
 * 以此类推
 */
public interface BinaryEnum extends Enums {

    int getBinaryNumber();
}
